export function ReadingSection() {
  return (
    <div>
      <H>沉浸阅读</H>
      <P>InkStream 也能当阅读器。打开 txt、Word（docx）、EPUB 电子书或 PDF，会切到一个全屏阅读界面：盖住编辑器与两侧面板，只留正文与一条简单工具栏。底层编辑器不卸载，关闭即回到原编辑状态。</P>
      <H>怎么进入</H>
      <Steps
        items={[
          <><strong>docx / epub / pdf</strong>：直接打开即自动进入阅读模式（它们不可编辑）。可经文件菜单打开、拖入窗口、或系统「打开方式」。</>,
          <><strong>txt</strong>：默认仍在编辑器打开；想换阅读视图时，视图 ▸「阅读模式」把当前 txt 切过去。</>,
        ]}
      />
      <H>工具栏</H>
      <Steps
        items={[
          <><strong>文体</strong>（小说 / 文献）：自动按正文特征识别并切换排版，识别不准时手动点选。</>,
          <><strong>配色</strong>（亮 / 护眼 / 夜间）：长时间阅读换护眼或夜间更舒服。</>,
          <><strong>字号</strong> −/＋ 调节正文大小；<strong>关闭</strong> 回到编辑器。</>,
        ]}
      />
      <Tip>小说排版用衬线字体、宽行距、段首缩进，接近纸质书；文献排版无缩进、紧凑行距、窄版心，接近论文。超大文档（约 100MB 以上）会被拒绝以保性能。</Tip>
    </div>
  );
}

export function BookshelfSection() {
  return (
    <div>
      <H>开启书架</H>
      <P>书架是阅读模式的书库，默认关闭。在 设置 ▸ 通用 ▸「书架」打开后，菜单栏出现「书架」入口。书架只是索引，保存在应用数据里，<strong>对源文件零改动</strong>；关闭书架只隐藏入口，已保存的数据不会删除，重新开启即恢复。</P>
      <H>导入书籍</H>
      <Steps
        items={[
          <>书架 ▸「导入书籍文件…」选一本或多本整本文件（txt / docx / epub / pdf）。</>,
          <>书架 ▸「导入书籍文件夹…」导入分卷 / 分章的文件夹，自动识别「书 → 卷 → 章」目录结构并按中文 / 数字顺序排好。</>,
          <>EPUB 自动提取封面，其余生成主题色标题卡，以画廊形式排列。</>,
        ]}
      />
      <H>阅读进度与续读</H>
      <Steps
        items={[
          <>阅读进度自动记录：txt/docx/epub 记到行 / 段、PDF 记到页，画廊封面下方的进度条一眼看到读到哪儿。</>,
          <>点封面打开即从上次的位置接着读；多卷多章的书还会回到上次在读的那一章，并可在阅读工具栏上一章 / 下一章。</>,
        ]}
      />
      <H>随手加入</H>
      <P>在阅读模式读一篇还没在架的文档时，工具栏有「加入书架」按钮；退出阅读模式时若该文档不在书架，也会弹窗问要不要加入，方便下次快速重读。</P>
      <Tip>书架命令也可在命令面板搜到（「书架：打开书架 / 导入书籍…」）。书架未开启时这些入口与命令一律隐藏。</Tip>
    </div>
  );
}

export function ExportSection() {
  return (
    <div>
      <H>导出当前文档</H>
      <P>文件菜单 ▸「导出为」选格式，把当前 Markdown 文档导出。转换全在本地完成，不需外部服务。也可在命令面板执行「文件：导出为 …」。</P>
      <Steps
        items={[
          <><strong>HTML / DOCX</strong>：弹系统保存对话框，选位置即落盘。</>,
          <><strong>PDF</strong>：弹系统打印对话框，在目标里选「另存为 PDF」保存。</>,
        ]}
      />
      <H>更多格式（需系统 pandoc）</H>
      <P>InkStream 不打包 pandoc，而是检测系统是否安装。装了 <K>pandoc</K> 后，「导出为」会多出 ODT / RTF / LaTeX / EPUB / Typst / Org 等格式（安装后需重启 InkStream 才出现）。</P>
      <H>内容与安全</H>
      <P>标题、强调、列表、引用、代码、表格、链接、图片都会正确转换；`$...$`/`$$...$$`/` ```math ` 经 KaTeX 渲染。导出产物会在浏览器打开，因此正文里的脚本与危险链接会被清理掉。</P>
      <H>水印与标识</H>
      <P>设置 ▸ 通用 ▸「导出水印页脚」可在文末附一行自定义水印（默认关闭），开后到「水印文字」填内容；留空则不附。生成器标识始终写入文件元数据（不可见），不影响正文。</P>
    </div>
  );
}
